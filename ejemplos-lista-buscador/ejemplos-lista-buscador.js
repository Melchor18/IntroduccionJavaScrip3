let orden = "ascendente"
function ascendente() {
    orden = "ascendente";
    actualizarLista();
}
function descendente() {
    orden = "descendente";
    actualizarLista();

}

function actualizarLista() {

    let listaOrdenada = [];
    if (orden === "ascendente") {
        listaOrdenada = datos.sort(
            (a, b) => {
                if (a.nombre_especie < b.nombre_especie) return -1;
                if (a.nombre_especie > b.nombre_especie) return 1;
                return 0;
            }
        )
    }
    else if (orden === "descendente") {
        listaOrdenada = datos.sort(
            (a, b) => {
                if (a.nombre_especie > b.nombre_especie) return -1;
                if (a.nombre_especie < b.nombre_especie) return 1;
                return 0;

            }
        )
    }
    else {
        listaOrdenada = datos
    }

    let textoBuscado = document.getElementById("barra-busqueda").value;

    let listaFiltrada = listaOrdenada.filter
        (animal => animal.nombre_especie.toLowerCase()
            .includes(textoBuscado.toLowerCase()));



document.getElementById("lista").innerHTML =
    listaFiltrada

        .map(
            x => `
        <div>
        ${x.nombre_especie} (${x.recuento})
        </div>
        `
        )
        .join("\n")
        }
        


